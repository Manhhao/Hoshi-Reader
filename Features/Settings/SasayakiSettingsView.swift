import SwiftUI

struct SasayakiSettingsView: View {
    @Environment(UserConfig.self) var userConfig
    var body: some View {
        @Bindable var userConfig = userConfig
        List {
            Section {
                Toggle("Enable", isOn: $userConfig.enableSasayaki)
            } footer: {
                VStack(alignment: .leading, spacing: 4) {
                    Text("Listen to audiobooks and read along with highlighted text.")
                    if userConfig.enableSasayaki {
                        Text("An **.srt** file generated by **[SubPlz](https://github.com/kanjieater/SubPlz)** and an audiobook (**.mp3**/**.m4b**) are required.")
                        Text("Long press a book and choose **Match**.")
                        Text("Choose the **.srt** and press **Match**. Matching only has to be done once.")
                        Text("You can now open the book and choose **Sasayaki** from the context menu.")
                        Text("Load the audiobook and press play, lines will be highlighted as the audiobook plays.")
                        Text("**[More...](https://github.com/Manhhao/Hoshi-Reader/blob/main/SASAYAKI.md)**")
                    }
                }
            }
            
            if userConfig.enableSasayaki {
                Section("Highlight") {
                    Toggle("Auto-Scroll", isOn: $userConfig.sasayakiAutoScroll)
                    ColorPicker("Text Color", selection: $userConfig.sasayakiTextColor)
                    ColorPicker("Background Color", selection: $userConfig.sasayakiBackgroundColor)
                }
                
                if userConfig.enableSync {
                    Section("Sync") {
                        Toggle("ッツ Sync", isOn: $userConfig.sasayakiEnableSync)
                    }
                }
            }
        }
        .navigationTitle("Sasayaki")
    }
}
